export type MessageKey = keyof typeof messages.en;

export function isUiLocale(value: string): value is UiLocale {
  return value === "en" || value === "fa";
}

export function normalizeLocale(value: string | undefined): UiLocale {
  const v = value ?? "";
  return isUiLocale(v) ? v : "en";
}

export function localeDir(locale: UiLocale): "ltr" | "rtl" {
  return locale === "fa" ? "rtl" : "ltr";
}

export function t(locale: UiLocale, key: MessageKey): string {
  return messages[locale][key] ?? messages.en[key];
}

export function formatMessage(
  locale: UiLocale,
  key: MessageKey,
  vars: Record<string, string>,
): string {
  let text = t(locale, key);
  for (const [name, value] of Object.entries(vars)) {
    text = text.split(`{${name}}`).join(value);
  }
  return text;
}

export function applyDocumentLocale(locale: UiLocale): void {
  const root = document.documentElement;
  root.lang = locale;
  root.dir = localeDir(locale);
}
